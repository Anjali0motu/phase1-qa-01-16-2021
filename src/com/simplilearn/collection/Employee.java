package com.simplilearn.collection;

public class Employee {
	
	public int id;
	public String name;
	public double salary;
	public String dept;
	
	public Employee(int id, String name, double salary, String dept) {
		super();
		this.id = id;
		this.name = name;
		this.salary = salary;
		this.dept = dept;
	}
	
	
}
